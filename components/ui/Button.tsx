'use client';

import React from 'react';
import { cva, type VariantProps } from 'class-variance-authority';
import { cn } from '@/lib/utils';

const buttonVariants = cva(
    'inline-flex items-center justify-center whitespace-nowrap rounded-full text-sm font-medium transition-all duration-300 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50',
    {
        variants: {
            variant: {
                default:
                    'bg-primary text-primary-foreground hover:bg-primary/90 shadow-md hover:shadow-lg hover:scale-105',
                secondary:
                    'bg-secondary text-secondary-foreground hover:bg-secondary/80',
                outline:
                    'border-2 border-primary text-primary bg-transparent hover:bg-primary hover:text-primary-foreground',
                ghost:
                    'text-foreground hover:bg-primary/10 hover:text-primary',
                link:
                    'text-primary underline-offset-4 hover:underline',
            },
            size: {
                default: 'h-12 px-6 py-3',
                sm: 'h-9 px-4 text-xs',
                lg: 'h-14 px-8 text-base',
                icon: 'h-10 w-10',
            },
        },
        defaultVariants: {
            variant: 'default',
            size: 'default',
        },
    }
);

export interface ButtonProps
    extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
    asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
    ({ className, variant, size, asChild = false, children, ...props }, ref) => {
        if (asChild) {
            const child = React.Children.only(children) as React.ReactElement<any>;
            return React.cloneElement(child, {
                ...(child.props as Record<string, unknown>),
                ...props,
                ref,
                className: cn(buttonVariants({ variant, size, className }), (child.props as any).className),
            } as any);
        }

        return (
            <button
                className={cn(buttonVariants({ variant, size, className }))}
                ref={ref}
                {...props}
            >
                {children}
            </button>
        );
    }
);

Button.displayName = 'Button';

export { Button, buttonVariants };
